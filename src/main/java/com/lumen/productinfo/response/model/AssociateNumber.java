package com.lumen.productinfo.response.model;

public class AssociateNumber {
	private String sec;
	private String dpa;
	private String associateNumber;

	public String getSec() {
		return sec;
	}

	public void setSec(String sec) {
		this.sec = sec;
	}

	public String getDpa() {
		return dpa;
	}

	public void setDpa(String dpa) {
		this.dpa = dpa;
	}

	public String getAssociateNumber() {
		return associateNumber;
	}

	public void setAssociateNumber(String associateNumber) {
		this.associateNumber = associateNumber;
	}

}
